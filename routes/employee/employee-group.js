exports.getEmployeeGroupInformation = async (req, res, next) => {

    var xresult = [];

    return (async () => {

        let lic_code = req.header('lic_code');
        let { emp_group_code, off_code, page_index, page_limit, action } = req.body[0];

        page_index = page_index == undefined ? 0 : page_index;
        page_limit = page_limit == undefined ? 10 : page_limit;
        if (page_index > 0) {
            page_index -= 1;
        }
        //เช็คเฉพาะส่วนที่สำคัญ
        if (emp_group_code == undefined || off_code == undefined || lic_code == undefined || action == undefined) {
            let response = [{
                status: 'error',
                invalid_code: '-1',
                message: 'ไม่สามารถดึงข้อมูลได้, เนื่องจากข้อมูลพารามิเตอร์ไม่ถูกต้อง',
                data: xresult,
                response_time: moment().format('YYYY-MM-DD HH:mm:ss')
            }]

            res.status(200).send(response);
            return;
        } else {

            let script = ``;
            if (emp_group_code.toString().toUpperCase() != 'ALL') {
                script = `select emp_group_code, emp_group_desc, emp_group_flag,
                ist_dt, mdf_dt, rm_dt from tbl_employee_group 
                where emp_group_flag = '1' and emp_group_code = '${emp_group_code}'`;
            }
            else {
                script = `select emp_group_code, emp_group_desc, emp_group_flag,
                ist_dt, mdf_dt, rm_dt from tbl_employee_group where emp_group_flag = '1'`;
            }

            if (off_code.toString().toUpperCase() != 'ALL') {
                script += ` and tbl_employee_group.off_code = '${off_code}'`
            }

            script += `  order by tbl_employee_group.ist_dt desc`
            script += ` limit ${page_limit} offset ${page_index * page_limit}`;

            let tbl_temporary = await pgConn.get(dbPrefix + lic_code, script, config.connectionString());
            if (!tbl_temporary.code) {
                //debugger
                if (tbl_temporary.data.length > 0) {
                    tbl_temporary.data = JSON.parse(JSON.stringify(tbl_temporary.data).replace(/\:null/gi, "\:\"\""));
                    let page_total = 0;
                    let rows_total = 0;
                    let script = ``
                    if (emp_group_code.toString().toUpperCase() != 'ALL') {
                        script = `select ceil((ceil(count(emp_group_code)) / ${page_limit})) as page_total, (count(emp_group_code)) as rows_total 
                    from tbl_employee_group 
                    where emp_group_flag = '1' and emp_group_code = '${emp_group_code}'`;
                    }
                    else {
                        script = `select ceil((ceil(count(emp_group_code)) / ${page_limit})) as page_total, (count(emp_group_code)) as rows_total 
                    from tbl_employee_group where emp_group_flag = '1'`;
                    }

                    if (off_code.toString().toUpperCase() != 'ALL') {
                        script += ` and tbl_employee_group.off_code = '${off_code}'`
                    }

                    let tbl_temporary_count = await pgConn.get(dbPrefix + lic_code, script, config.connectionString());
                    if (!tbl_temporary_count.code) {
                        if (tbl_temporary_count.data.length > 0) {
                            page_total = parseInt(tbl_temporary_count.data[0].page_total);
                            rows_total = parseInt(tbl_temporary_count.data[0].rows_total);
                        }
                    }
                    let response = [{
                        status: 'success',
                        invalid_code: '0',
                        message: '',
                        data: tbl_temporary.data,
                        page_total: page_total,
                        rows_total: rows_total,
                        response_time: moment().format('YYYY-MM-DD HH:mm:ss')
                    }]

                    res.status(200).send(response);
                    return;
                } else {
                    let response = [{
                        status: 'success',
                        invalid_code: '0',
                        message: '',
                        data: xresult,
                        response_time: moment().format('YYYY-MM-DD HH:mm:ss')
                    }]

                    res.status(200).send(response);
                    return;
                }
            } else {
                let response = [{
                    status: 'error',
                    invalid_code: '-3',
                    message: `ไม่สามารถดึงข้อมูล, กรุณาติดต่อเจ้าหน้าที่ผู้ดูแลระบบ`,
                    data: xresult,
                    response_time: moment().format('YYYY-MM-DD HH:mm:ss')
                }]
                res.status(200).send(response);
                await xglobal.action_logs(lic_code, action[0].id, 'ดึงข้อมูลกลุ่มพนักงาน', JSON.stringify(req.body[0]), 'ไม่สามารถดึงข้อมูล, กรุณาติดต่อเจ้าหน้าที่ผู้ดูแลระบบ', action[0].value);
                return;
            }
        }

    })().catch(async (err) => {
        console.log(err);
        let response = [{
            status: 'error',
            invalid_code: '-4',
            message: `ไม่สามารถดึงข้อมูล, กรุณาติดต่อเจ้าหน้าที่ผู้ดูแลระบบ`,
            data: xresult,
            response_time: moment().format('YYYY-MM-DD HH:mm:ss').toString()
        }]
        res.status(200).send(response);
        await xglobal.action_logs(lic_code, action[0].id, 'ดึงข้อมูลกลุ่มพนักงาน', JSON.stringify(req.body[0]), 'ไม่สามารถดึงข้อมูล, กรุณาติดต่อเจ้าหน้าที่ผู้ดูแลระบบ', action[0].value);
        return;
    });

}

